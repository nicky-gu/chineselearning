export function getRandomHanzi(count: number = 10) {
  const shuffled = [...HANZI_DATA].sort(() => Math.random() - 0.5)
  return shuffled.slice(0, count)
}

export function getHanziByChar(char: string) {
  return HANZI_DATA.find(h => h.char === char)
}

export function getHanziByPinyin(pinyin: string) {
  return HANZI_DATA.filter(h => h.pinyin.toLowerCase() === pinyin.toLowerCase())
}
